"""Generate a workspace-tier PODS glossary skill from interview answers JSON.

Usage:
    python generate_glossary.py \
        --customer acme-midstream \
        --answers answers.json \
        --output /Workspace/.assistant/skills/acme-midstream-pods-glossary/SKILL.md

The answers.json shape is documented in interview.md. The most important output
is the per-column UNIT table — without it, the analytical PODS skills can join
feet to meters silently.
"""
from __future__ import annotations

import argparse
import json
import sys
from datetime import date
from pathlib import Path


SKILL_TEMPLATE = """---
name: {customer}-pods-glossary
description: |
  Customer-specific PODS schema mappings for {customer_proper}: physical
  table/column names -> canonical PODS concepts, the UNIT of every measure
  column, which modules are adopted, the route key, and business jargon (line
  and segment names). Use whenever a PODS / pipeline-integrity question touches
  {customer_proper} data. Compose with the pods-* enterprise skills.
metadata:
  version: "0.1.0"
parent: pods-overview
---

# {customer_proper} — PODS Glossary

Generated by `pods-setup` on {generated_at}. Edit when schema, units, modules, or business terms change.

{sections}

## How to use

When you encounter a {customer_proper} pipeline term or need a physical column:
1. Check this glossary first — especially the UNIT of any measure column before comparing it to another.
2. If the mapping is here, use it. If not, ASK before guessing.
3. If a module is not in the adopted list, tell the user that data isn't available rather than querying missing tables.
4. If you learn a new mapping during the session, suggest adding it via a re-run of `pods-setup`.
"""


def _md_table(headers: list[str], rows: list[list[str]]) -> str:
    if not rows:
        return "_(none captured)_"
    lines = ["| " + " | ".join(headers) + " |", "|" + "|".join("---" for _ in headers) + "|"]
    lines += ["| " + " | ".join(row) + " |" for row in rows]
    return "\n".join(lines)


def _section_route_key(rk: dict) -> str:
    body = "## Route key\n\n"
    if not rk:
        return body + "_(not captured — REQUIRED, ask before any route-measure query)_"
    body += f"Canonical `route_id` maps to physical `{rk.get('physical', '?')}`.\n\n"
    if not rk.get("consistent_across_tables", True):
        body += "⚠️ Route key differs across tables:\n\n"
        rows = [[f"`{t}`", f"`{c}`"] for t, c in rk.get("per_table", {}).items()]
        body += _md_table(["Table", "Route-key column"], rows)
    return body


def _section_units(units: dict) -> str:
    body = ("## Measure column UNITS (critical)\n\n"
            "Confirm the unit before comparing any two measure columns. "
            "Convert to meters with `pods_ft_to_m` before joins.\n\n")
    rows = [[f"`{col}`", f"**{unit}**"] for col, unit in units.items()]
    return body + _md_table(["Column", "Unit"], rows)


def _section_ili(ili: dict) -> str:
    body = "## ILI (anomalies + runs)\n\n"
    if not ili:
        return body + "_(not captured)_"
    body += f"- **Anomaly table**: `{ili.get('anomaly_table', '?')}`\n"
    cols = ili.get("columns", {})
    if cols:
        body += "- **Anomaly columns**: " + ", ".join(f"{k}=`{v}`" for k, v in cols.items()) + "\n"
    body += f"- **ERF stored?**: {'yes — ' + ili.get('erf_column', '?') if ili.get('erf_stored') else 'no — compute via B31G/RSTRENG UDFs'}\n"
    body += f"- **Runs table**: `{ili.get('runs_table', '?')}`\n"
    rcols = ili.get("run_columns", {})
    if rcols:
        body += "- **Run columns**: " + ", ".join(f"{k}=`{v}`" for k, v in rcols.items()) + "\n"
    mlt = ili.get("metal_loss_tool_types", [])
    if mlt:
        body += "- **Metal-loss tools** (latest-run selection): " + ", ".join(f"`{t}`" for t in mlt) + "\n"
    tol = ili.get("tool_tolerance_pct_wall", {})
    if tol:
        body += "- **Tool tolerance (±%wall)**: " + ", ".join(f"{k}={v}" for k, v in tol.items()) + "\n"
    else:
        body += "- **Tool tolerance**: _not captured — assessments fall back to call depth_\n"
    return body.rstrip()


def _section_integrity(cfg: dict) -> str:
    body = "## Integrity assessment configuration\n\n"
    if not cfg:
        return body + ("_(not captured — defaults: 3× wall clustering; confirm safety factor and "
                       "response thresholds before any dig query — never default them silently)_")
    body += f"- **Interaction window**: {cfg.get('interaction_window_rule', '3x_wall (default)')}\n"
    sf = cfg.get("safety_factor")
    body += f"- **Safety factor (ERF)**: {sf if sf is not None else '_unknown — confirm, do not default_'}\n"
    rt = cfg.get("response_thresholds", {})
    if rt:
        body += ("- **Response thresholds (ILLUSTRATIVE — tie to 49 CFR 195.452(h)/192.933 + B31.8S and "
                 f"the IM program)**: immediate ERF ≥ {rt.get('immediate_erf', '?')}, "
                 f"scheduled ERF ≥ {rt.get('scheduled_erf', '?')}\n")
    return body.rstrip()


def _section_pipe_attrs(pa: dict) -> str:
    body = "## Pipe attributes (for integrity math)\n\n"
    if not pa:
        return body + "_(not captured — B31G/ERF will be unavailable)_"
    rows = [[k, f"`{v}`"] for k, v in pa.items()]
    return body + _md_table(["Attribute", "Column"], rows)


def _section_modules(mods: list[str]) -> str:
    body = "## Modules adopted\n\n"
    if not mods:
        return body + "_(not captured — assume only core + ILI until confirmed)_"
    all_mods = ["IR", "TVC", "ILI", "CP", "SL", "OFF"]
    rows = [[m, "✅ adopted" if m in mods else "— not adopted"] for m in all_mods]
    return body + _md_table(["Module", "Status"], rows)


def _section_lines(lines: dict) -> str:
    body = "## Line / segment business names\n\n"
    rows = [[f"`{name}`", ", ".join(f"`{v}`" for v in ids)] for name, ids in lines.items()]
    return body + _md_table(["Business term", "route_id values"], rows)


def _section_flow(flow: str) -> str:
    body = "## Flow direction\n\n"
    if not flow:
        return body + "_(not captured — confirm before upstream/downstream queries)_"
    return body + f"`{flow}` — use this to interpret upstream/downstream."


def _section_tribal(facts: list[str]) -> str:
    body = "## Tribal knowledge\n\n"
    if not facts:
        return body + "_(none captured)_"
    return body + "\n".join(f"- {f}" for f in facts)


SECTIONS = [
    ("route_key", _section_route_key, {}),
    ("measure_units", _section_units, {}),
    ("ili", _section_ili, {}),
    ("pipe_attributes", _section_pipe_attrs, {}),
    ("integrity_assessment", _section_integrity, {}),
    ("modules_adopted", _section_modules, []),
    ("lines", _section_lines, {}),
    ("flow_direction", _section_flow, ""),
    ("tribal_knowledge", _section_tribal, []),
]


def render(answers: dict) -> str:
    customer = answers["customer"]
    sections = "\n\n".join(renderer(answers.get(key, default)) for key, renderer, default in SECTIONS)
    return SKILL_TEMPLATE.format(
        customer=customer,
        customer_proper=customer.replace("-", " ").title(),
        generated_at=date.today().isoformat(),
        sections=sections,
    )


def main() -> int:
    ap = argparse.ArgumentParser(description="Generate a PODS workspace glossary skill.")
    ap.add_argument("--customer", required=True, help="Short kebab-case customer name.")
    ap.add_argument("--answers", required=True, type=Path, help="Path to answers.json (see interview.md).")
    ap.add_argument("--output", required=True, type=Path, help="Output SKILL.md path.")
    args = ap.parse_args()

    answers = json.loads(args.answers.read_text())
    answers["customer"] = args.customer  # CLI overrides file
    rendered = render(answers)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(rendered)
    print(f"wrote {args.output} ({len(rendered):,} chars)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
