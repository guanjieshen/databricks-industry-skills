"""Apply standardized Unity Catalog table and column comments to Maximo Silver tables.

Reads a JSON file describing the canonical Maximo MBO descriptions and either
(a) emits a runnable SQL file for SQL Editor / Pro-warehouse execution
(--emit-sql), or (b) writes directly to UC via the Databricks SDK (--apply).

SAFETY: PREVIEW (no writes) is the default. UC is modified ONLY when --apply is
passed, which must never be used without the customer's explicit approval of
the previewed statements (per the multi-checkpoint vetting flow in SKILL.md).

Usage:
    # 1) PREVIEW (default — safe, no writes). Prints the statements + (optionally)
    #    writes them to a runnable .sql file for SQL Editor execution:
    python apply_uc_comments.py --catalog eam --schema maximo_silver \
        --comments-file maximo_comments.json \
        --emit-sql apply_uc_comments.sql

    # 2) ONLY after explicit user approval at Checkpoint 2 of the vetted flow,
    #    apply via Python SDK:
    python apply_uc_comments.py --catalog eam --schema maximo_silver \
        --comments-file maximo_comments.json --apply --warehouse-id <id>

    # 3) Scope by module activity (default: active). With a profile file,
    #    DORMANT modules are excluded by default; pass --scope all to include them:
    python apply_uc_comments.py --catalog eam --schema maximo_silver \
        --comments-file maximo_comments.json \
        --profile-path /Workspace/Users/.../draft_profile.json \
        --scope active|all \
        --emit-sql apply_uc_comments.sql

The comments file shape (see maximo_comments.json):
    { "WORKORDER": { "table_comment": "...",
                     "columns": { "WONUM": "...", ... } },
      ... }

The SQL artifact uses Databricks parameter markers (`:catalog`, `:silver_schema`)
via IDENTIFIER() for the namespace, so the customer binds them in SQL Editor at
execution time. The committed apply_uc_comments.sql is generated by running this
script with --emit-sql; CI verifies the committed copy matches.
"""
